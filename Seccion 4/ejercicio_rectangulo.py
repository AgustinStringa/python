base = int(input('Introduce el valor de la base del rectangulo en cm '))
altura = int(input('Introduce el valor de la altura del rectangulo en cm '))


perimetro = (base * 2) + (altura * 2)
area = base * altura
print(f'el perimetro del rectangulo es {perimetro}')
print(f'el area del rectangulo es {area}')
